package com.csmarton.model;

import java.util.Date;

class DateRange {
    Date from;
    Date to;

    public DateRange() {
    }

    public Date getFrom() {
        return from;
    }

    public void setFrom(Date from) {
        this.from = from;
    }

    public Date getTo() {
        return to;
    }

    public void setTo(Date to) {
        this.to = to;
    }
}
